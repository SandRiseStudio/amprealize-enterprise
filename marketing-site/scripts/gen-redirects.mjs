/**
 * Emit Cloudflare Pages `_redirects` so /wiki/* (and optionally /) passthrough
 * to the SPA that actually serves the public wiki (Cloudflare project
 * `amprealize-web`), until wiki content ships in this repo.
 *
 * Set PUBLIC_LEGACY_WIKI_BASE at build time (default empty = no passthrough).
 * Using https://amprealize.ai as the base while apex points at *this* Pages
 * project creates a self-redirect loop on /wiki/* — use
 * https://amprealize-web.pages.dev (or another origin that serves the wiki).
 *
 * Set PUBLIC_LEGACY_WIKI_REDIRECT_ROOT=0 to omit `/` → wiki when shipping a
 * real marketing homepage on apex (M3+).
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const publicDir = path.join(root, 'public');
const rawBase = process.env.PUBLIC_LEGACY_WIKI_BASE ?? '';
const base = rawBase.replace(/\/+$/, '');
const redirectRoot = process.env.PUBLIC_LEGACY_WIKI_REDIRECT_ROOT !== '0';

fs.mkdirSync(publicDir, { recursive: true });
const lines = [`# Generated by scripts/gen-redirects.mjs — legacy wiki passthrough`];

if (base) {
  lines.push(`/wiki/*  ${base}/wiki/:splat  302`);
  if (redirectRoot) {
    lines.push(`/  ${base}/wiki/infra  302`);
  }
}
lines.push('');

fs.writeFileSync(path.join(publicDir, '_redirects'), lines.join('\n'), 'utf8');
if (base) {
  console.log(`Wrote public/_redirects → /wiki/* → ${base}/wiki/*${redirectRoot ? '; / → ' + base + '/wiki/infra' : ''}`);
} else {
  console.log('Wrote public/_redirects (no wiki passthrough — PUBLIC_LEGACY_WIKI_BASE unset)');
}
