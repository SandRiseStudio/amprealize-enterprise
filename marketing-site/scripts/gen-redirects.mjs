/**
 * Emit Cloudflare Pages `_redirects` so /wiki/* proxies to the legacy public wiki
 * until wiki content is bundled or hosted on docs.amprealize.ai.
 *
 * Set PUBLIC_LEGACY_WIKI_BASE at build time (default https://amprealize.ai).
 * After apex DNS cuts over to this project, point the base at docs or remove
 * once static wiki ships under this repo.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const publicDir = path.join(root, 'public');
const base = (process.env.PUBLIC_LEGACY_WIKI_BASE || 'https://amprealize.ai').replace(/\/+$/, '');

fs.mkdirSync(publicDir, { recursive: true });
const lines = [
  `# Generated by scripts/gen-redirects.mjs — legacy wiki passthrough`,
  `/wiki/*  ${base}/wiki/:splat  302`,
  '',
];
fs.writeFileSync(path.join(publicDir, '_redirects'), lines.join('\n'), 'utf8');
console.log(`Wrote public/_redirects → wiki/* → ${base}/wiki/*`);
