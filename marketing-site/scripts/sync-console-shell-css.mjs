/**
 * Concatenate console sidebar/shell + wiki sidebar CSS into one file for the
 * marketing wiki shell (keeps look aligned with web-console).
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const wc = path.join(root, '..', 'web-console', 'src');
const outFile = path.join(root, 'src', 'styles', 'console-shell.bundle.css');

const chunks = [
  // design-system.css first — canonical source of all CSS tokens so the
  // sidebar/shell CSS variables resolve identically to the console.
  path.join(wc, 'styles', 'design-system.css'),
  path.join(wc, 'components', 'workspace', 'WorkspaceShell.css'),
  path.join(wc, 'components', 'sidebar', 'SidebarNav.css'),
  path.join(wc, 'components', 'wiki', 'Wiki.css'),
];

function main() {
  const parts = ['/* Auto-generated by scripts/sync-console-shell-css.mjs — do not edit */\n'];
  for (const file of chunks) {
    if (!fs.existsSync(file)) {
      console.warn(`[sync-console-shell-css] Skip missing ${file}`);
      continue;
    }
    parts.push(`\n/* === ${path.relative(path.join(root, '..'), file)} === */\n`);
    parts.push(fs.readFileSync(file, 'utf8'));
  }
  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, parts.join(''), 'utf8');
  console.log(`[sync-console-shell-css] Wrote ${path.relative(root, outFile)}`);
}

main();
